package testo.roed.simulation.model;

public class Result {

	private int homescore;
	private int roadscore;

	public Result() {
	}

	public void setHomescore(int score) {
		homescore = score;

	}

	public void setRoadscore(int score) {
		roadscore = score;

	}

	public Object getHomescore() {
		return homescore;
	}

	public Object getRoadscore() {
		return roadscore;
	}


}
